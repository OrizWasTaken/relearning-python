# Conditional Tests

# Tests expected to be True
car = 'subaru'
print("Is car == 'subaru'? I predict True.")
print(car == 'subaru')

fruit = 'apple'
print("\nIs fruit == 'apple'? I predict True.")
print(fruit == 'apple')

number = 10
print("\nIs number == 10? I predict True.")
print(number == 10)

language = 'python'
print("\nIs language == 'python'? I predict True.")
print(language == 'python')

age = 18
print("\nIs age >= 18? I predict True.")
print(age >= 18)

